import { app, BrowserWindow, ipcMain } from 'electron'
import { existsSync } from 'fs'
import { join } from 'path'
import { autoUpdater } from 'electron-updater'
import { isAutoGeneratedChangelogOnly, plainTextReleaseNotes } from './releaseNotes'

const GITHUB_OWNER = 'JRNCarrizo'
const GITHUB_REPO = 'bodegaStock'

export type UpdateStatusPayload =
  | { type: 'checking' }
  | { type: 'available'; version: string; releaseNotes?: string }
  | { type: 'not-available'; version: string }
  | { type: 'download-progress'; percent: number; transferred: number; total: number }
  | { type: 'downloaded'; version: string }
  | { type: 'error'; message: string }

function sendStatus(win: BrowserWindow, status: UpdateStatusPayload) {
  if (!win.isDestroyed()) {
    win.webContents.send('update-status', status)
  }
}

let handlersRegistered = false

function configureAutoUpdaterFeed(): boolean {
  if (!app.isPackaged) return false

  const updateYml = join(process.resourcesPath, 'app-update.yml')
  if (existsSync(updateYml)) return true

  autoUpdater.setFeedURL({
    provider: 'github',
    owner: GITHUB_OWNER,
    repo: GITHUB_REPO
  })
  return true
}

function friendlyUpdateError(err: unknown): string {
  const message = err instanceof Error ? err.message : String(err)

  if (message.includes('app-update.yml')) {
    return 'Esta versión no incluye actualizaciones automáticas. Descargá la última versión desde GitHub Releases e instalá manualmente.'
  }

  if (message.includes('404') || message.includes('Not Found')) {
    return 'No se encontró un release compatible en GitHub. Descargá el instalador manualmente desde Releases.'
  }

  return message
}

export function setupAutoUpdater(getWindow: () => BrowserWindow | null) {
  if (handlersRegistered) return
  handlersRegistered = true

  autoUpdater.autoDownload = false
  autoUpdater.autoInstallOnAppQuit = true

  autoUpdater.on('checking-for-update', () => {
    const win = getWindow()
    if (win) sendStatus(win, { type: 'checking' })
  })

  autoUpdater.on('update-available', (info) => {
    const win = getWindow()
    if (!win) return
    const rawNotes =
      typeof info.releaseNotes === 'string'
        ? info.releaseNotes
        : Array.isArray(info.releaseNotes)
          ? info.releaseNotes.map((n) => (typeof n === 'string' ? n : n.note)).join('\n')
          : undefined
    const notes = rawNotes ? plainTextReleaseNotes(rawNotes) : undefined
    const releaseNotes =
      notes && !isAutoGeneratedChangelogOnly(notes) ? notes : undefined
    sendStatus(win, { type: 'available', version: info.version, releaseNotes })
  })

  autoUpdater.on('update-not-available', (info) => {
    const win = getWindow()
    if (win) sendStatus(win, { type: 'not-available', version: info.version })
  })

  autoUpdater.on('download-progress', (progress) => {
    const win = getWindow()
    if (win) {
      sendStatus(win, {
        type: 'download-progress',
        percent: progress.percent,
        transferred: progress.transferred,
        total: progress.total
      })
    }
  })

  autoUpdater.on('update-downloaded', (info) => {
    const win = getWindow()
    if (win) sendStatus(win, { type: 'downloaded', version: info.version })
  })

  autoUpdater.on('error', (err) => {
    const win = getWindow()
    if (win) sendStatus(win, { type: 'error', message: friendlyUpdateError(err) })
  })

  ipcMain.handle('app:get-info', () => ({
    version: app.getVersion(),
    name: app.getName(),
    isPackaged: app.isPackaged,
    platform: process.platform,
    updatesConfigured: app.isPackaged
      ? existsSync(join(process.resourcesPath, 'app-update.yml'))
      : false
  }))

  ipcMain.handle('update:check', async () => {
    if (!app.isPackaged) {
      return { ok: false as const, reason: 'dev' as const }
    }

    configureAutoUpdaterFeed()

    try {
      const result = await autoUpdater.checkForUpdates()
      return { ok: true as const, updateInfo: result?.updateInfo?.version }
    } catch (err) {
      return {
        ok: false as const,
        reason: 'error' as const,
        message: friendlyUpdateError(err)
      }
    }
  })

  ipcMain.handle('update:download', async () => {
    if (!app.isPackaged) {
      return { ok: false as const, reason: 'dev' as const }
    }

    configureAutoUpdaterFeed()

    try {
      await autoUpdater.downloadUpdate()
      return { ok: true as const }
    } catch (err) {
      return {
        ok: false as const,
        message: friendlyUpdateError(err)
      }
    }
  })

  ipcMain.handle('update:install', () => {
    if (!app.isPackaged) {
      return { ok: false as const, reason: 'dev' as const }
    }
    autoUpdater.quitAndInstall()
    return { ok: true as const }
  })
}
